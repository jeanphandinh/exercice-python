# Exercice 4 — Strings

phrase = "python est un langage puissant"

print("Phrase d'origine :", phrase)
print("Majuscules :", phrase.upper())
print("Première lettre en majuscule :", phrase.capitalize())
print("Nombre de caractères :", len(phrase))
print("Remplacement :", phrase.replace("puissant", "utile"))
